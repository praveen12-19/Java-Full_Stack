public class Threegreater{
    public static void main(String[] args) {
        int p=12;
        int q=15;
        int r=18;
        if(p>q && p>r){
            System.out.println("p is greater");
        
        }
        else if(q>r && q>p){
            System.out.println("q is greater");
        
        }
        else{
            System.out.println("r is greater");
        }
    }
    
}
